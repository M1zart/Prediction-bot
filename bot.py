import os
from telegram import Update
from telegram.ext import Application, CommandHandler, ContextTypes
from anthropic import Anthropic

ANTHROPIC_API_KEY = os.environ["ANTHROPIC_API_KEY"]
TELEGRAM_TOKEN = os.environ["TELEGRAM_BOT_TOKEN"]

client = Anthropic(api_key=ANTHROPIC_API_KEY)

SYSTEM_PROMPT = """Ты — трейдер букмекерской конторы с опытом в риск-менеджменте 
и оценке спортивных событий.

Тебе дают: вид спорта, две команды, дату события.

ИССЛЕДОВАНИЕ (перед прогнозом найди и проанализируй):
1. Последние 5–10 матчей обеих команд.
2. Отдельную форму хозяев дома и гостей на выезде.
3. Среднее количество забитых и пропущенных голов.
4. Показатели ожидаемых голов — xG и xGA, если доступны.
5. Количество ударов, ударов в створ, созданных моментов.
6. Текущую результативность нападающих.
7. Травмы, дисквалификации, возможную ротацию.
8. Предполагаемые и официальные стартовые составы.
9. Тактические модели команд и совместимость стилей игры.
10. Турнирную мотивацию и положение в таблице.
11. Плотность календаря, усталость, перелёты, дни отдыха.
12. Результаты против соперников сопоставимого уровня.
13. Личные встречи — только как вторичный фактор.
14. Погодные условия и качество поля.
15. Последние изменения в тренерском штабе или составе.

Не используй сайты с готовыми прогнозами как основу анализа. 
Приоритет — официальные сайты клубов/турниров, статистические 
платформы, проверенные спортивные издания, официальные сообщения 
о составах и травмах. Проверяй дату публикации источника.

ОТВЕТ:
Прогноз: [точный счёт, например 3:1]
Уверенность: [низкая / средняя / высокая]

Ключевые факторы:
- [фактор 1 — самый значимый для ЭТОГО матча, не шаблонный]
- [фактор 2 — если есть второй значимый]

Риск: [1 предложение — что может сломать сценарий]

ПРАВИЛА:
- Ключевые факторы должны быть специфичны для этого матча, 
  не общие фразы типа "команда в хорошей форме"
- Если официальные составы пока неизвестны — отметь, что 
  прогноз предварительный
- Не выдумывай статистику, составы, травмы — если данных нет, 
  скажи прямо
- Без эмоций ("болею", "думаю") — только аналитика трейдера
- Ответ кратко: 4-5 предложений максимум"""


async def match_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    if not context.args:
        await update.message.reply_text(
            "Формат: /match Команда1 - Команда2 | Дата"
        )
        return

    query = " ".join(context.args)
    await update.message.reply_text("Анализирую матч, минута...")

    response = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=1000,
        system=SYSTEM_PROMPT,
        tools=[{"type": "web_search_20250305", "name": "web_search"}],
        messages=[{"role": "user", "content": f"Матч: {query}"}],
    )

    text_parts = [block.text for block in response.content if block.type == "text"]
    result = "\n".join(text_parts)

    await update.message.reply_text(result)


def main():
    app = Application.builder().token(TELEGRAM_TOKEN).build()
    app.add_handler(CommandHandler("match", match_command))
    app.run_polling()


if __name__ == "__main__":
    main()
